<?php defined('SYSPATH') or die('No direct script access.');

// define some constants that make it easier to add line endings
if ( ! defined('EOL')) {
	/**
	*   CONST :: end of line
	*   @var    string
	*/
	define('EOL', "\r\n");
}

if ( ! defined('HEOL')) {
	/**
	*   CONST :: HTML line ending with new line
	*   @var    string
	*/
	define('HEOL', "<br />\r\n");
}

if ( ! defined('TAB')) {
	/**
	*   CONST :: HTML line ending with new line
	*   @var    string
	*/
	define('TAB', "\t");
}